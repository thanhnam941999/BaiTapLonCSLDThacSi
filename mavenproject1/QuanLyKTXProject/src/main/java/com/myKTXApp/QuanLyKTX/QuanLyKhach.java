    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String tuNgay = jTextField6.getText().trim();
        String denNgay = jTextField7.getText().trim();

        if (tuNgay.isEmpty() || denNgay.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ ngày bắt đầu và kết thúc.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            loadTableDataByDateRange(tuNgay, denNgay);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi tra cứu: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void loadTableDataByDateRange(String tuNgay, String denNgay) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        try {
            String sql = "SELECT * FROM KhachThamQuan WHERE NgayDen BETWEEN '" + tuNgay + "' AND '" + denNgay + "' ORDER BY NgayDen";
            ResultSet rs = MySqlConnect.executeSQL(sql);
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("Ten"),
                    rs.getString("CMT"),
                    rs.getDate("NgaySinh"),
                    rs.getString("MaSV"),
                    rs.getDate("NgayDen"),
                    "Sửa",
                    "Xóa"
                });
            }
            if (model.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Không tìm thấy kết quả nào trong khoảng thời gian này.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi tải dữ liệu: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }
    
