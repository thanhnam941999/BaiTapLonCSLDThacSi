    private void loadTableData() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        try {
            ResultSet rs = MySqlConnect.layKhachThamQuan(); // Giả sử bạn đã tạo phương thức này trong MySqlConnect
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("Ten"),
                    rs.getString("CMT"),
                    rs.getDate("NgaySinh"),
                    rs.getString("MaSV"),
                    rs.getDate("NgayDen"),
                    "Sửa",
                    "Xóa"
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi tải dữ liệu: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    // Sự kiện lắng nghe khi click vào các nút sửa và xóa trên bảng
    private void setupTableListeners() {
        jTable1.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent e) {
                int column = jTable1.columnAtPoint(e.getPoint());
                int row = jTable1.rowAtPoint(e.getPoint());
                if (row < 0 || column < 0) return;
                String columnName = jTable1.getColumnName(column);
                if ("Sửa".equals(columnName)) {
                    editKhach(row);
                } else if ("Xóa".equals(columnName)) {
                    deleteKhach(row);
                }
            }
        });
    }
    
    private void deleteKhach(int row) {
        String tenKhach = (String) jTable1.getValueAt(row, 0); // Giả sử cột đầu tiên là Tên Khách
        String cmt = (String) jTable1.getValueAt(row, 1); // Lấy số CMT để xác định duy nhất khách
        int confirm = JOptionPane.showConfirmDialog(this,
            "Bạn có chắc chắn muốn xóa thông tin khách " + tenKhach + "?", "Xác nhận xóa",
            JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                // Thực hiện xóa khách trong cơ sở dữ liệu
                boolean success = MySqlConnect.deleteKhach(cmt);
                if (success) {
                    JOptionPane.showMessageDialog(this, "Đã xóa thông tin khách thành công.");
                    // Sau khi xóa, cập nhật lại bảng
                    loadTableData();
                } else {
                    JOptionPane.showMessageDialog(this, "Không thể xóa thông tin khách. Vui lòng thử lại.", "Lỗi", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Lỗi khi xóa thông tin khách: " + ex.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    
    private void editKhach(int row) {
        currentCMT = (String) jTable1.getValueAt(row, 1);
        jTextField1.setText((String) jTable1.getValueAt(row, 0));  // Tên khách
        jTextField2.setText((String) jTable1.getValueAt(row, 1));  // CMT
        jTextField3.setText(jTable1.getValueAt(row, 2).toString()); // Ngày sinh
        jTextField4.setText((String) jTable1.getValueAt(row, 3));  // Mã SV được thăm
        jTextField5.setText(jTable1.getValueAt(row, 4).toString()); // Ngày thăm
        jButton1.setText("Cập nhật");
        // Lưu ID của khách đang được chỉnh sửa (nếu cần)
        // currentKhachId = (Integer) jTable1.getValueAt(row, hiddenIdColumnIndex);
    }
    
