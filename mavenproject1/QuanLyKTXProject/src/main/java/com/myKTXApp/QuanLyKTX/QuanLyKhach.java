    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String ten = jTextField1.getText().trim();
        String cmt = jTextField2.getText().trim();
        String ngaySinh = jTextField3.getText().trim();
        String maSV = jTextField4.getText().trim();
        String ngayDen = jTextField5.getText().trim();

        if (ten.isEmpty() || cmt.isEmpty() || ngaySinh.isEmpty() || maSV.isEmpty() || ngayDen.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String sql;
        boolean isUpdating = !currentCMT.isEmpty();
        if (isUpdating) {
            // Cập nhật khách hiện có
            sql = "UPDATE KhachThamQuan SET Ten = '" + ten + "', CMT = '" + cmt + "', NgaySinh = '" + ngaySinh + 
                  "', MaSV = '" + maSV + "', NgayDen = '" + ngayDen + 
                  "' WHERE CMT = '" + currentCMT + "'";
        } else {
            // Thêm khách mới
            sql = "INSERT INTO KhachThamQuan (Ten, CMT, NgaySinh, MaSV, NgayDen) VALUES ('" + 
                  ten + "', '" + cmt + "', '" + ngaySinh + "', '" + maSV + "', '" + ngayDen + "')";
        }

        try {
            int rowsAffected = MySqlConnect.executeUpdate(sql);
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Dữ liệu khách thăm quan đã được " + 
                                              (isUpdating ? "cập nhật" : "thêm") + 
                                              " thành công.", "Thành công!", JOptionPane.INFORMATION_MESSAGE);
                clearFields();
                loadTableData();
                currentCMT = "";
                jButton1.setText("Thêm khách");
            } else {
                JOptionPane.showMessageDialog(this, "Không có dòng nào được thay đổi.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

