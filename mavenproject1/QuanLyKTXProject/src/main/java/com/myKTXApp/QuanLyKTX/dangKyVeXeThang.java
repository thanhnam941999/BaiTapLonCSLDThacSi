    private boolean kiemTraSinhVien(String maSV) throws SQLException {
    String query = "SELECT * FROM SinhVien WHERE MaSV = ?";
    try (Connection conn = MySqlConnect.getConnection();
         PreparedStatement pstmt = conn.prepareStatement(query)) {
        pstmt.setString(1, maSV);
        ResultSet rs = pstmt.executeQuery();
        if (!rs.next()) {
            JOptionPane.showMessageDialog(this, "Sinh viên không tồn tại.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }
    return true;
}

private boolean kiemTraSoXeDangKy(String maSV) throws SQLException {
    String query = "SELECT COUNT(*) FROM XeDangKy WHERE MaSV = ?";
    try (Connection conn = MySqlConnect.getConnection();
         PreparedStatement pstmt = conn.prepareStatement(query)) {
        pstmt.setString(1, maSV);
        ResultSet rs = pstmt.executeQuery();
        if (rs.next() && rs.getInt(1) >= 2) {
            JOptionPane.showMessageDialog(this, "Sinh viên đã đăng ký tối đa 2 xe.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }
    return true;
}

private boolean isValidDate(String dateStr) {
    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
    sdf.setLenient(false); // Đặt chế độ nghiêm ngặt
    try {
        sdf.parse(dateStr);
        return true;
    } catch (ParseException e) {
        return false;
    }
}

private boolean dangKyXe(String maSV, String bienSo, Date ngayDangKy) throws SQLException {
    String query = "INSERT INTO XeDangKy (MaSV, BienSo, NgayDangKy) VALUES (?, ?, ?)";
    try (Connection conn = MySqlConnect.getConnection();
         PreparedStatement pstmt = conn.prepareStatement(query)) {
        pstmt.setString(1, maSV);
        pstmt.setString(2, bienSo);
        pstmt.setDate(3, new java.sql.Date(ngayDangKy.getTime()));
        int affectedRows = pstmt.executeUpdate();
        return affectedRows > 0;
    }
}

