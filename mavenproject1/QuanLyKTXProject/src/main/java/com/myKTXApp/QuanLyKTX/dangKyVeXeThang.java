    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String maSV = jTextField1.getText().trim();
        String bienSo = jTextField2.getText().trim();
        String ngayDangKyStr = jTextField3.getText().trim();

        if (maSV.isEmpty() || bienSo.isEmpty() || ngayDangKyStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Kiểm tra ngày hợp lệ
        if (!isValidDate(ngayDangKyStr)) {
            JOptionPane.showMessageDialog(this, "Ngày không hợp lệ. Vui lòng nhập theo định dạng yyyy-MM-dd", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            Date ngayDangKy = sdf.parse(ngayDangKyStr);

            if (kiemTraSinhVien(maSV) && kiemTraSoXeDangKy(maSV) && dangKyXe(maSV, bienSo, ngayDangKy)) {
                JOptionPane.showMessageDialog(this, "Đăng ký vé xe tháng thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                clearFields();
            }
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Ngày không hợp lệ. Vui lòng nhập theo định dạng yyyy-MM-dd", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage(), "Lỗi Database", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

